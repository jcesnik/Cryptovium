////////////////////////////////////////////////////////////////////////////////
void hkdf_extract(
	const uint8_t*	salt,
	size_t			salt_len,
	const uint8_t*	input_key_material,
	size_t			input_key_material_len,
	uint8_t			prk[SHA256_HASH_SIZE])
{
	sha256_context	ctx;

	sha256_hmac_init(&ctx, salt, salt_len);
	sha256_hmac_update(&ctx, input_key_material, input_key_material_len);
	sha256_hmac_finish(&ctx, prk);
}

////////////////////////////////////////////////////////////////////////////////
void hkdf_expand(
	size_t			n_start_offset,	// 0 by default
	const uint8_t	prk[SHA256_HASH_SIZE],
  const uint8_t*	info,
	size_t			info_len,
	uint8_t*		okm,
	size_t			okm_len)
{
	sha256_context	ctx;
	uint8_t			step_buffer[SHA256_HASH_SIZE];
	bool			step_buffer_flag = false;
	size_t			n, i, idx = 0;
	uint8_t			c = 0;
	size_t			step_size = 0;
	size_t			remaining = okm_len;
	
	n = okm_len / SHA256_HASH_SIZE;
	
	if ((okm_len % SHA256_HASH_SIZE) != 0)
		n++;

	for (i = 0; i < n_start_offset + n; ++i)
	{
		sha256_hmac_init(&ctx, prk, SHA256_HASH_SIZE);
		
		if (step_buffer_flag == true)
			sha256_hmac_update(&ctx, step_buffer, SHA256_HASH_SIZE);
		else
			step_buffer_flag = true;
		
		sha256_hmac_update(&ctx, info, info_len);
		c++;
		sha256_hmac_update(&ctx, &c, sizeof(uint8_t));
		sha256_hmac_finish(&ctx, step_buffer);
		
		if (i >= n_start_offset)
		{
			step_size = (remaining < SHA256_HASH_SIZE ? remaining : SHA256_HASH_SIZE);
			memcpy(&okm[idx], step_buffer, step_size);
			idx += step_size;
			remaining -= step_size;
		}
	}
	
	zeroize(step_buffer, sizeof(step_buffer));
}

////////////////////////////////////////////////////////////////////////////////
void hkdf_derive_secrets(
	size_t			n_start_offset,	// 0 by default
	const uint8_t*	salt,
	size_t			salt_len,
	const uint8_t*	input_key_material,
	size_t			input_key_material_len,
	const uint8_t*	info,
	size_t			info_len,
	uint8_t*		output,
	size_t			output_len)
{
	uint8_t		prk[SHA256_HASH_SIZE];
	
	hkdf_extract(salt, salt_len, input_key_material, input_key_material_len, prk);
	hkdf_expand(n_start_offset, prk, info, info_len, output, output_len);
	zeroize(prk, sizeof(prk));
}

